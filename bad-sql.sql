WITH DEPT_SAL AS (
    SELECT
        DEPARTMENT_ID,
        SUM(SALARY) TOTAL_SAL,
        AVG(SALARY) AVG_SAL
    FROM HR.EMPLOYEES
    GROUP BY DEPARTMENT_ID
)

SELECT *
FROM DEPT_SAL DS
WHERE AVG_SAL > (SELECT AVG(SALARY) FROM HR.EMPLOYEES)
ORDER BY 1;


SELECT
    EMP.EMPLOYEE_ID,
    EMP.FIRST_NAME,
    EMP.LAST_NAME,
    (
        SELECT COUNT(*) FROM HR.EMPLOYEES E2
        WHERE E2.MANAGER_ID = EMP.EMPLOYEE_ID
    ) AS TEAMSIZE
FROM HR.EMPLOYEES EMP
WHERE
    EMP.EMPLOYEE_ID IN
    (
        SELECT EMPLOYEE_ID FROM HR.EMPLOYEES
        WHERE SALARY > 9000
    )
    AND EMP.DEPARTMENT_ID NOT IN (
        SELECT DEPARTMENT_ID FROM HR.DEPARTMENTS
        WHERE LOCATION_ID = 1700
    );


SELECT
    DEPARTMENT_ID,
    SUM(CASE WHEN SALARY > 10000 THEN 1 ELSE 0 END) HIGH_PAID,
    SUM(CASE WHEN SALARY <= 10000 THEN 1 ELSE 0 END) LOW_PAID
FROM HR.EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING SUM(SALARY) > 50000
ORDER BY DEPARTMENT_ID;
