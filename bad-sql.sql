SELECT
    E.EMPLOYEE_ID,
    E.FIRST_NAME,
    E.LAST_NAME,
    E.SALARY,
    E.DEPARTMENT_ID,
    D.DEPARTMENT_NAME,
    CASE
        WHEN E.SALARY > 10000 THEN 'HIGH'
        ELSE 'LOW'
    END SALARY_LEVEL,
    COALESCE(E.COMMISSION_PCT, 0) COMM,
    SYSDATE AS RUNDATE
FROM HR.EMPLOYEES E
LEFT JOIN HR.DEPARTMENTS D
    ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
WHERE
    1 = 1
    AND E.DEPARTMENT_ID IN (10, 20, 30, 40)
    AND SALARY > 5000
    AND (
        E.JOB_ID = 'IT_PROG'
        OR JOB_ID = 'SA_MAN'
    )
    AND E.HIRE_DATE < TO_DATE('2018-01-01', 'YYYY-MM-DD')
ORDER BY DEPARTMENT_ID ASC, SALARY DESC;


